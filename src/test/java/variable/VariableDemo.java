package variable;

public class VariableDemo {
    public static void main(String[] args) {

        // Data Type: Represents a data
        // Whole Numbers: -1, 0, 19                             int
        int age = -1;

        // Decimal Numbers: 14.76, -0.76, 3.14                  double
        double pi = 3.141592;

        // Character: 'A', 'y', '%'                             char
        char grade = 'C';

        // Text: "Alphabet", "Hello There", "Jack's Demos"      String
        String words = "Let's just code.";

        // Logic: true, false  bool
        boolean isRaining = true;

        System.out.println(pi);
        System.out.println(pi + age - grade + " " + words);
        System.out.println(grade);
        System.out.println(isRaining);
    }
}
